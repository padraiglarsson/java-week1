public class Tut3 {
    public static void main(String[] args) {

        // Declare and initialize a variable 'y' with the value 3
        int y = 3;

        // Check if the expression (4 * y - 5) is equal to 11
        if (4 * y - 5 == 11) {
            // If true, print "Correct"
            System.out.println("Correct");
        } else {
            // If false, print "Wrong! the correct answer is 4"
            System.out.println("Wrong! The correct answer is 4");
        }
    }
}

